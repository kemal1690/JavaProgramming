package day03_EscapeSequences;
/*
escape sequences: MUST be given with the double quote
\n : starts a new line
\t : paragraph space (tab)
\\ : single back slash
\" : double quote

 */
public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Java \nPython \nC#");

        System.out.println("----------------------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt is nice to see you all! \nWhat class do we have next week");

        System.out.println("-------------------------");

        System.out.println("\tJava is Cool Programming Language");

        System.out.println("---------------------");
        System.out.println("//\\\\");

        System.out.println("/ \\"); //in order to give one backward slash we need to give two backward slashes

        System.out.println("My favourite TV show is \"Game of Throne\"");// in order to print double quote we need to use \"



    }






}
